"""
analyze_clinical_annotation.py
================================
Analyses the completed clinical annotation CSV and computes agreement
statistics between the ontology's constraint C2 flag and independent
clinician judgment.

Outputs
-------
  outputs/clinical_validation_results.txt  -- human-readable summary
  outputs/clinical_validation_stats.csv    -- machine-readable statistics

Usage
-----
  python analyze_clinical_annotation.py

Required input files (both in outputs/)
----------------------------------------
  annotation_blind.csv  -- returned by clinician with Clinical_Judgment filled
  annotation_key.csv    -- generated by prepare_clinical_annotation.py

Two-annotator mode
------------------
If two clinicians annotated independently, save their files as:
  annotation_blind_annotator1.csv
  annotation_blind_annotator2.csv
and set TWO_ANNOTATORS = True below.

Judgment codes
--------------
  P  Plausible  -- configuration is biomechanically consistent
  S  Suspicious -- configuration is biomechanically unusual or inconsistent
  U  Uncertain  -- insufficient information or genuinely borderline

Primary analysis excludes Uncertain ratings. A sensitivity analysis
treating Uncertain as Plausible is also reported.

References
----------
Landis, J.R. & Koch, G.G. (1977). The measurement of observer agreement
for categorical data. Biometrics, 33(1), 159-174.
"""

import io
import os
import sys
import contextlib

import numpy as np
import pandas as pd
from sklearn.metrics import cohen_kappa_score, confusion_matrix

# -----------------------------------------------------------------------
# CONFIG
# -----------------------------------------------------------------------
OUT_DIR         = "outputs"
BLIND_PATH      = os.path.join(OUT_DIR, "annotation_blind.csv")
KEY_PATH        = os.path.join(OUT_DIR, "annotation_key.csv")
TWO_ANNOTATORS  = False

ANNOTATOR1_PATH = os.path.join(OUT_DIR, "annotation_blind_annotator1.csv")
ANNOTATOR2_PATH = os.path.join(OUT_DIR, "annotation_blind_annotator2.csv")


# -----------------------------------------------------------------------
# STEP 1: Load and merge annotation with key
# -----------------------------------------------------------------------
def load_and_merge(blind_path: str, key_path: str) -> pd.DataFrame:
    blind = pd.read_csv(blind_path)
    key   = pd.read_csv(key_path)

    blind["Clinical_Judgment"] = (
        blind["Clinical_Judgment"]
        .astype(str).str.strip().str.upper()
    )

    valid   = {"P", "S", "U"}
    invalid = set(blind["Clinical_Judgment"].unique()) - valid
    if invalid:
        print(f"WARNING: Unexpected judgment values found: {invalid}")
        print("  Expected only P, S, or U. Please check the annotation file.")

    merged = key.merge(
        blind[["Annotation_ID", "Clinical_Judgment"]],
        on="Annotation_ID",
        how="left"
    )

    n_missing = merged["Clinical_Judgment"].isna().sum()
    if n_missing > 0:
        print(f"WARNING: {n_missing} rows have no clinical judgment "
              f"and will be excluded from analysis.")

    return merged


# -----------------------------------------------------------------------
# STEP 2: Compute agreement statistics
# -----------------------------------------------------------------------
def compute_agreement(df: pd.DataFrame, label: str = "Overall") -> dict:
    """
    Computes Cohen's kappa, precision, recall, F1, and specificity
    comparing the C2 flag against clinician judgment.

    Convention
    ----------
    C2 = 1, Clinician = S  ->  True Positive  (C2 correctly flags suspicious)
    C2 = 1, Clinician = P  ->  False Positive (C2 flags a plausible instance)
    C2 = 0, Clinician = S  ->  False Negative (C2 misses a suspicious instance)
    C2 = 0, Clinician = P  ->  True Negative  (C2 correctly clears a plausible)

    Parameters
    ----------
    df    : merged annotation DataFrame (output of load_and_merge)
    label : name for this subset, used in printed output

    Returns
    -------
    dict of computed statistics
    """
    res = {"label": label, "n_total": len(df)}

    # ---- Primary analysis: exclude Uncertain ----
    primary = df[df["Clinical_Judgment"].isin(["P", "S"])].copy()
    primary["c2_bin"]       = primary["c2_violated"].astype(int)
    primary["clinical_bin"] = (primary["Clinical_Judgment"] == "S").astype(int)

    res["n_uncertain"] = len(df) - len(primary)
    res["n_analysed"]  = len(primary)

    if len(primary) < 10:
        print(f"  [{label}] Too few non-Uncertain instances ({len(primary)}) "
              f"for reliable statistics.")
        return res

    kappa = cohen_kappa_score(primary["clinical_bin"], primary["c2_bin"])
    res["kappa_primary"] = round(kappa, 3)
    res["kappa_interp"]  = _interpret_kappa(kappa)

    cm = confusion_matrix(
        primary["clinical_bin"],
        primary["c2_bin"],
        labels=[1, 0]
    )
    TP, FN = int(cm[0, 0]), int(cm[0, 1])
    FP, TN = int(cm[1, 0]), int(cm[1, 1])
    res.update({"TP": TP, "FP": FP, "FN": FN, "TN": TN})

    precision   = TP / (TP + FP) if (TP + FP) > 0 else 0.0
    recall      = TP / (TP + FN) if (TP + FN) > 0 else 0.0
    f1          = (2 * precision * recall / (precision + recall)
                   if (precision + recall) > 0 else 0.0)
    specificity = TN / (TN + FP) if (TN + FP) > 0 else 0.0
    agreement   = (TP + TN) / len(primary)

    res.update({
        "precision":         round(precision,   3),
        "recall":            round(recall,      3),
        "f1":                round(f1,          3),
        "specificity":       round(specificity, 3),
        "agreement":         round(agreement,   3),
    })

    # ---- Sensitivity analysis: Uncertain treated as Plausible ----
    sens = df.copy()
    sens["c2_bin"]       = sens["c2_violated"].astype(int)
    sens["clinical_bin"] = (sens["Clinical_Judgment"] == "S").astype(int)
    res["kappa_sensitivity"] = round(
        cohen_kappa_score(sens["clinical_bin"], sens["c2_bin"]), 3
    )

    return res


def _interpret_kappa(k: float) -> str:
    """Landis & Koch (1977) kappa interpretation."""
    if k < 0.00: return "less than chance"
    if k < 0.20: return "slight"
    if k < 0.40: return "fair"
    if k < 0.60: return "moderate"
    if k < 0.80: return "substantial"
    return "almost perfect"


# -----------------------------------------------------------------------
# STEP 3: Inter-rater agreement (two annotators)
# -----------------------------------------------------------------------
def compute_interrater(
    path1: str, path2: str, key_path: str
) -> tuple[pd.DataFrame, dict]:
    """
    Computes inter-rater agreement between two annotators and returns
    a merged DataFrame using majority vote (disagreements -> Uncertain).
    """
    a1  = pd.read_csv(path1)
    a2  = pd.read_csv(path2)
    key = pd.read_csv(key_path)

    for df in [a1, a2]:
        df["Clinical_Judgment"] = (
            df["Clinical_Judgment"].astype(str).str.strip().str.upper()
        )

    merged = key.merge(
        a1[["Annotation_ID", "Clinical_Judgment"]].rename(
            columns={"Clinical_Judgment": "J_A1"}),
        on="Annotation_ID"
    ).merge(
        a2[["Annotation_ID", "Clinical_Judgment"]].rename(
            columns={"Clinical_Judgment": "J_A2"}),
        on="Annotation_ID"
    )

    both_clear = merged[
        merged["J_A1"].isin(["P", "S"]) &
        merged["J_A2"].isin(["P", "S"])
    ].copy()

    a1_bin = (both_clear["J_A1"] == "S").astype(int)
    a2_bin = (both_clear["J_A2"] == "S").astype(int)

    irr_kappa  = cohen_kappa_score(a1_bin, a2_bin)
    agree_rate = (a1_bin == a2_bin).mean()

    irr = {
        "irr_kappa":     round(irr_kappa, 3),
        "irr_agree_pct": round(100 * agree_rate, 1),
        "irr_interp":    _interpret_kappa(irr_kappa),
        "n_both_clear":  len(both_clear),
    }

    print(f"\n=== Inter-rater agreement ===")
    print(f"Instances with clear judgment from both: {irr['n_both_clear']}")
    print(f"Percent agreement : {irr['irr_agree_pct']}%")
    print(f"Cohen's kappa     : {irr['irr_kappa']}  ({irr['irr_interp']})")

    # Disagreements treated as Uncertain in the main analysis
    merged["Clinical_Judgment"] = merged.apply(
        lambda r: r["J_A1"] if r["J_A1"] == r["J_A2"] else "U",
        axis=1
    )

    return merged, irr


# -----------------------------------------------------------------------
# STEP 4: Print results
# -----------------------------------------------------------------------
def print_results(res: dict, subgroups: list = None):
    print(f"\n{'='*60}")
    print(f"CLINICAL VALIDATION RESULTS — {res['label']}")
    print(f"{'='*60}")
    print(f"Total instances annotated : {res['n_total']}")
    print(f"  Uncertain (U) excluded  : {res['n_uncertain']}")
    print(f"  Analysed (P or S only)  : {res['n_analysed']}")

    if "kappa_primary" not in res:
        print("  (insufficient data for statistics)")
        return

    print(f"\nCohen's kappa (primary)    : {res['kappa_primary']}"
          f"  [{res['kappa_interp']}]")
    print(f"Cohen's kappa (U=Plausible): {res['kappa_sensitivity']}")
    print()
    print("Confusion matrix (rows = clinician judgment, cols = C2 flag):")
    print(f"                   C2=1 (flagged)   C2=0 (not flagged)")
    print(f"  Clinician: S     {res['TP']:>13}   {res['FN']:>13}")
    print(f"  Clinician: P     {res['FP']:>13}   {res['TN']:>13}")
    print()
    print(f"Precision  (C2 vs clinician): {res['precision']:.3f}")
    print(f"Recall     (C2 vs clinician): {res['recall']:.3f}")
    print(f"F1 score                    : {res['f1']:.3f}")
    print(f"Specificity                 : {res['specificity']:.3f}")
    print(f"Overall agreement rate      : {res['agreement']:.3f}")

    if subgroups:
        print(f"\n--- Subgroup breakdown ---")
        for sg in subgroups:
            kap = sg.get("kappa_primary", "N/A")
            pre = sg.get("precision",     "N/A")
            rec = sg.get("recall",        "N/A")
            print(f"  {sg['label']:<12}  kappa={kap}  "
                  f"precision={pre}  recall={rec}")


# -----------------------------------------------------------------------
# STEP 5: Save outputs
# -----------------------------------------------------------------------
def save_outputs(overall: dict, subgroups: list, irr: dict = None):
    """Saves plain-text summary and CSV of statistics. No manuscript content."""
    os.makedirs(OUT_DIR, exist_ok=True)

    # Plain-text summary
    buf = io.StringIO()
    with contextlib.redirect_stdout(buf):
        print_results(overall, subgroups)
        if irr:
            print(f"\nInter-rater kappa     : {irr['irr_kappa']}"
                  f"  ({irr['irr_interp']})")
            print(f"Inter-rater agreement : {irr['irr_agree_pct']}%"
                  f"  (n = {irr['n_both_clear']})")
    summary = buf.getvalue()

    txt_path = os.path.join(OUT_DIR, "clinical_validation_results.txt")
    with open(txt_path, "w", encoding="utf-8") as f:
        f.write(summary)

    # Machine-readable CSV
    rows = [
        {"subset": r["label"],
         **{k: v for k, v in r.items() if k != "label"}}
        for r in [overall] + subgroups
    ]
    if irr:
        rows.append({"subset": "inter-rater", **irr})

    csv_path = os.path.join(OUT_DIR, "clinical_validation_stats.csv")
    pd.DataFrame(rows).to_csv(csv_path, index=False, encoding="utf-8")

    print(f"\nOutputs saved:")
    print(f"  {txt_path}")
    print(f"  {csv_path}")


# -----------------------------------------------------------------------
# MAIN
# -----------------------------------------------------------------------
if __name__ == "__main__":

    for path, name in [(BLIND_PATH, "annotation_blind.csv"),
                       (KEY_PATH,   "annotation_key.csv")]:
        if not os.path.exists(path):
            print(f"ERROR: {name} not found at {path}")
            sys.exit(1)

    irr_stats = None

    if TWO_ANNOTATORS:
        print("Two-annotator mode...")
        merged, irr_stats = compute_interrater(
            ANNOTATOR1_PATH, ANNOTATOR2_PATH, KEY_PATH
        )
    else:
        print("Loading annotation and key files...")
        merged = load_and_merge(BLIND_PATH, KEY_PATH)

    # Judgment distribution
    jdist = merged["Clinical_Judgment"].value_counts()
    print(f"\nJudgment distribution:")
    for val, cnt in jdist.items():
        print(f"  {val}: {cnt} ({100.0 * cnt / len(merged):.1f}%)")

    # Overall statistics
    overall = compute_agreement(merged, label="Overall")
    print_results(overall)

    # Subgroup statistics by object shape
    subgroups = []
    for shape in ["Cylinder", "Sphere"]:
        sub = merged[merged["shape"].str.lower() == shape.lower()].copy()
        if len(sub) >= 10:
            sg = compute_agreement(sub, label=shape)
            subgroups.append(sg)
    if subgroups:
        print("\n--- Subgroup results ---")
        for sg in subgroups:
            print_results(sg)

    save_outputs(overall, subgroups, irr_stats)
